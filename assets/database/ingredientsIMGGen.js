const fs = require('fs');
const path = require('path');

const ingredientsDir = path.join(__dirname, '../ingredients');
const outputFile = path.join(__dirname, 'ingredientsPath.js');

// Get all .png files in the ingredients directory
const files = fs.readdirSync(ingredientsDir).filter(file => file.endsWith('.png'));

let output = `// This file is auto-generated by ingredientsIMGGen.js\n\n`;

// Export each image as a constant
files.forEach(file => {
    const name = path.basename(file, '.png');
    output += `export const ${name} = require('../../assets/ingredients/${file}');\n`;
});

// Optionally, export all as an object
output += `\nexport default {\n`;
files.forEach(file => {
    const name = path.basename(file, '.png');
    output += `  ${name},\n`;
});
output += `};\n`;

fs.writeFileSync(outputFile, output);

console.log('ingredientsPath.js generated successfully.');